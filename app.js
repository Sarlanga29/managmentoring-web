const loginDialog = document.querySelector("#loginDialog");
const portal = document.querySelector("#portal");
const portalRole = document.querySelector("#portalRole");
const portalTitle = document.querySelector("#portalTitle");
const clientView = document.querySelector('[data-view="client"]');
const adminView = document.querySelector('[data-view="admin"]');
const table = document.querySelector("#clientTable");
const detail = document.querySelector("#clientDetail");
const search = document.querySelector("#clientSearch");
let currentLanguage = getInitialLanguage();
let currentRole = null;

function getInitialLanguage() {
  const params = new URLSearchParams(window.location.search);
  const urlLanguage = params.get("lang");
  const storedLanguage = localStorage.getItem("managmentoring-language");
  const browserLanguage = navigator.language?.slice(0, 2);
  return [urlLanguage, storedLanguage, browserLanguage].find((language) => translations[language]) || "es";
}

function setLanguage(language, shouldUpdateUrl = true) {
  currentLanguage = translations[language] ? language : "es";
  const dictionary = translations[currentLanguage];
  document.documentElement.lang = currentLanguage;
  document.title = dictionary.metaTitle;
  document.querySelector('meta[name="description"]').setAttribute("content", dictionary.metaDescription);
  localStorage.setItem("managmentoring-language", currentLanguage);

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (dictionary[key]) {
      element.textContent = dictionary[key];
    }
  });

  document.querySelectorAll("[data-i18n-attr]").forEach((element) => {
    element.dataset.i18nAttr.split(",").forEach((pair) => {
      const [attribute, key] = pair.split(":");
      if (attribute && key && dictionary[key]) {
        element.setAttribute(attribute, dictionary[key]);
      }
    });
  });

  document.querySelectorAll("[data-lang]").forEach((button) => {
    const isActive = button.dataset.lang === currentLanguage;
    button.classList.toggle("active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  if (currentRole) {
    updatePortalLabels(currentRole);
  }

  if (!adminView.classList.contains("hidden")) {
    const filteredClients = filterClients();
    renderClients(filteredClients);
    renderDetail(filteredClients[0] || getEmptyClient());
  }

  if (shouldUpdateUrl) {
    const url = new URL(window.location.href);
    url.searchParams.set("lang", currentLanguage);
    window.history.replaceState({}, "", url);
  }
}

function openLogin() {
  if (typeof loginDialog.showModal === "function") {
    loginDialog.showModal();
  }
}

function setRole(role) {
  currentRole = role;
  portal.classList.remove("hidden");
  if (loginDialog.open) {
    loginDialog.close();
  }
  const isAdmin = role === "admin";
  updatePortalLabels(role);
  clientView.classList.toggle("hidden", isAdmin);
  adminView.classList.toggle("hidden", !isAdmin);
  portal.scrollIntoView({ behavior: "smooth", block: "start" });
  if (isAdmin) {
    renderClients(clients);
    renderDetail(clients[0]);
  }
}

function updatePortalLabels(role) {
  const dictionary = translations[currentLanguage];
  const isAdmin = role === "admin";
  portalRole.textContent = isAdmin ? dictionary.backofficePrivate : dictionary.participantPortalLabel;
  portalTitle.textContent = isAdmin ? dictionary.adminPanelTitle : dictionary.clientJourneyTitle;
}

function filterClients() {
  const term = search.value.toLowerCase();
  return clients.filter((client) =>
    [client.name, client.company, client.program, client.status].join(" ").toLowerCase().includes(term)
  );
}

function renderClients(items) {
  table.innerHTML = "";
  items.forEach((client, index) => {
    const row = document.createElement("button");
    row.type = "button";
    row.className = `client-row${index === 0 ? " active" : ""}`;
    row.innerHTML = `
      <strong>${client.name}<span>${client.company}</span></strong>
      <span>${client.program}</span>
      <span>${client.status}</span>
      <span>${client.next}</span>
    `;
    row.addEventListener("click", () => {
      document.querySelectorAll(".client-row").forEach((item) => item.classList.remove("active"));
      row.classList.add("active");
      renderDetail(client);
    });
    table.appendChild(row);
  });
}

function renderDetail(client) {
  const dictionary = translations[currentLanguage];
  detail.innerHTML = `
    <span class="status-pill">${client.status}</span>
    <h3>${client.name}</h3>
    <p>${client.company} · ${client.program}</p>
    <dl>
      <div><dt>${dictionary.detailNext}</dt><dd>${client.next}</dd></div>
      <div><dt>${dictionary.detailProgress}</dt><dd>${client.progress}</dd></div>
      <div><dt>${dictionary.detailNotes}</dt><dd>${client.notes}</dd></div>
      <div><dt>${dictionary.detailMaterials}</dt><dd>${client.materials}</dd></div>
    </dl>
  `;
}

function getEmptyClient() {
  const dictionary = translations[currentLanguage];
  return {
    name: dictionary.noResults,
    company: dictionary.adjustSearch,
    program: "",
    status: dictionary.clientBase,
    next: "-",
    progress: "-",
    notes: dictionary.noResultsNotes,
    materials: "-",
  };
}

document.querySelectorAll("[data-open-login]").forEach((button) => {
  button.addEventListener("click", openLogin);
});

document.querySelectorAll("[data-login-as]").forEach((button) => {
  button.addEventListener("click", () => setRole(button.dataset.loginAs));
});

document.querySelectorAll("[data-lang]").forEach((button) => {
  button.addEventListener("click", () => setLanguage(button.dataset.lang));
});

document.querySelector("[data-logout]").addEventListener("click", () => {
  portal.classList.add("hidden");
  currentRole = null;
  document.querySelector("#privado").scrollIntoView({ behavior: "smooth", block: "start" });
});

search.addEventListener("input", () => {
  const filtered = filterClients();
  renderClients(filtered);
  renderDetail(filtered[0] || getEmptyClient());
});

setLanguage(currentLanguage, false);
